    private void infoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoActionPerformed
        
        if(colocacao.getSelectedRow()== -1)
        {
            JOptionPane.showMessageDialog(rootPane,"É preciso selecionar uma Linha da Tabela Colocação!");
        }  
        else
       {
           Colocacao ob =(Colocacao) modelotab.getValueAt(colocacao.getSelectedRow(),0);
            JOptionPane.showMessageDialog(rootPane,ob.getInfo());
       }
        
    }//GEN-LAST:event_infoActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        
        if(colocacao.getSelectedRow()== -1)
        {
            JOptionPane.showMessageDialog(rootPane,"É preciso selecionar uma Linha da Tabela Colocação!");
            
        }
        else
        {
            Colocacao ob =(Colocacao)modelotab.getValueAt(colocacao.getSelectedRow(),0);
            int opexc= JOptionPane.showConfirmDialog(rootPane,"Tem certeza que deseja excluir o jogador "
                    +ob+" da Tabela Colocação?");
            if(opexc== JOptionPane.YES_OPTION)
            {
                (new Velhadao()).excluir(ob);
                    carregarColocacao();  

            }
        }
        
    }//GEN-LAST:event_delActionPerformed

