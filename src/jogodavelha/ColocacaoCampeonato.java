    public void carregarColocacao()
    {
        
        modelotab.setNumRows(0);
        Velhadao dao= new Velhadao();
        for(Colocacao obj: dao.buscarCol())
        {
           
            modelotab.addRow(new Object[]{obj,obj.getJogador().getPontuacao(),
                obj.getCampeonato().getNome(),obj.getCampeonato().getData()});
           
        }
    }

